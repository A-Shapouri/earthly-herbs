export function sortProductsByPrice(products: Array<any>, order: 'asc' | 'desc' = "asc") {
  return products.sort((a, b) => {
    const priceA = parseFloat(a.price);
    const priceB = parseFloat(b.price);

    if (order === "asc") {
      return priceA - priceB; // Ascending order
    } else if (order === "desc") {
      return priceB - priceA; // Descending order
    }
  });
}

export function searchProductsByTagsAndOptionalCategory(products:Array<any>,tags:Array<string>, category: string|null = null) {
  return products.filter(product => {
    const matchesTags = tags.length ? tags.some(tag => product.tag === tag): true;
    const matchesCategory = category ? product.category === category : true;
    return matchesTags && matchesCategory;
  });
}

export function filterProductsByPriceRange(products: Array<any>,minPrice: number, maxPrice: number) {
  return products.filter(product => {
    const price = parseFloat(product.price);
    return price >= minPrice && price <= maxPrice;
  });
}

export function filterProductsByCaffeineLevel(products: Array<any>, levels: Array<number>) {
  return products.filter(product => levels.includes(product.caffeineLevel));
}
export function sortByDiscount(products: Array<any>) {
  return products.sort((a, b) => {
    const discountA = a.discount !== null ? a.discount : 0;
    const discountB = b.discount !== null ? b.discount : 0;
    return discountB - discountA;
  });
}